#include <bits/stdc++.h>
using namespace std;

int main() {
    int A, B;
    cin >> A >> B;

    int i = 0;
    int j = 0;

    cout << "A:";
    while (i < A) {
        cout << "]";
        i ++;
    }
    cout << endl;

    cout << "B:";
    while (j < B) {
        cout << "]";
        j ++;
    }
    cout << endl;
}
